package com.kintone.client.model.app.report;

/** An enum for representing the time unit used for the "Group by" option of Graphs. */
public enum AggregationTimeUnit {

    /** by the year */
    YEAR,

    /** by the quarter */
    QUARTER,

    /** by the month */
    MONTH,

    /** by the week */
    WEEK,

    /** by the day */
    DAY,

    /** by the hour */
    HOUR,

    /** by the minute */
    MINUTE
}

package com.kintone.client.model.app.report;

/** An enum for representing the chart type of Graphs. */
public enum ChartType {

    /** Bar chart */
    BAR,

    /** Column chart */
    COLUMN,

    /** Pie chart */
    PIE,

    /** Line chart */
    LINE,

    /** Pivot table */
    PIVOT_TABLE,

    /** Table */
    TABLE,

    /** Area chart */
    AREA,

    /** Spline chart */
    SPLINE,

    /** Spline area chart */
    SPLINE_AREA
}
